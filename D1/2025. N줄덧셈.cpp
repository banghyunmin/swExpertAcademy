#include<iostream>

using namespace std;

int main(int argc, char** argv)
{
    /////////////////////////////////////////////////////////////////////////////////////////////
    
    int num;
    cin >> num;
    
    int res = 0;
    for(int i = 1; i <= num; i++) {
        res += i;
    }
    
    cout << res << endl;
    
    /////////////////////////////////////////////////////////////////////////////////////////////
    
	return 0;//정상종료시 반드시 0을 리턴해야합니다.
}